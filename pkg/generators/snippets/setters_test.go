func TestGenerateSetterForPrimitiveType(t *testing.T) {
	t.Parallel()

	ctx, err := newTestGeneratorContext()
	require.NoError(t, err)

	tests := []struct {
		description string
		testStruct  *types.Type
		want        string
	}{
		{
			description: "use pointer receiver",
			testStruct:  newTestType(t, "SomeStruct"),
			want: `// WithName is an autogenerated function
func (o *SomeStruct) WithName(in string) *SomeStruct {
	o.ObjectMeta.Name = in
	return o
}

`,
		},
		{
			description: "use value receiver",
			testStruct:  newTestType(t, "ValueStruct"),
			want: `// WithName is an autogenerated function
func (o ValueStruct) WithName(in string) ValueStruct {
	o.ObjectMeta.Name = in
	return o
}

`,
		},
	}

	for _, test := range tests {
		parent := getMemberFromType(t, test.testStruct, "MockStruct", "ObjectMeta").Type
		member := getMemberFromType(t, test.testStruct, "MockStruct", "ObjectMeta", "Name")

		var b bytes.Buffer
		setter := NewSetter(test.testStruct, parent)
		sw := generator.NewSnippetWriter(&b, ctx, "$", "$")
		sw.Do(setter.GenerateSetterForPrimitiveType(member))
		assert.NoError(t, sw.Error())
		assert.Equal(t, test.want, b.String())
	}
}

