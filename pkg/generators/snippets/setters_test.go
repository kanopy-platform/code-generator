func TestFuncName(t *testing.T) {
	t.Parallel()

	tests := []struct {
		description string
		name        string
		kind        types.Kind
		elem        *types.Type
		want        string
	}{
		{
			description: "Setter for Builtin type",
			name:        "Name",
			kind:        types.Builtin,
			want:        "WithName",
		},
		{
			description: "Setter for Struct type",
			name:        "Struct",
			kind:        types.Struct,
			want:        "WithStruct",
		},
		{
			description: "Setter for String Slice type",
			name:        "StringSlice",
			kind:        types.Slice,
			elem:        types.String,
			want:        "AppendStringSlice",
		},
		{
			description: "Setter for Byte Slice type",
			name:        "ByteSlice",
			kind:        types.Slice,
			elem:        types.Byte,
			want:        "WithByteSlice",
		},
	}

	for _, test := range tests {
		m := types.Member{
			Name: test.name,
			Type: &types.Type{
				Kind: test.kind,
				Elem: test.elem,
			},
		}

		assert.Equal(t, test.want, funcName(m), test.description)
	}
}

func TestMemberAccessor(t *testing.T) {
	t.Parallel()

	someStruct := newTestType(t, "SomeStruct")

	tests := []struct {
		description string
		parent      *types.Type
		member      types.Member
		want        string
	}{
		{
			description: "Root and Parent are the same, access member directly",
			parent:      someStruct,
			member:      getMemberFromType(t, someStruct, "SomeStruct", "AStruct"),
			want:        "AStruct",
		},
		{
			description: "Root and Parent are different, need intermediary to access member",
			parent:      getMemberFromType(t, someStruct, "SomeStruct", "ObjectMeta").Type,
			member:      getMemberFromType(t, someStruct, "SomeStruct", "ObjectMeta", "Name"),
			want:        "ObjectMeta.Name",
		},
	}

	for _, test := range tests {
		setter := NewSetter(someStruct, test.parent, true)
		assert.Equal(t, test.want, setter.memberAccessor(test.member), test.description)
	}
}
