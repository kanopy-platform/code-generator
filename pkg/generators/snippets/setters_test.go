func TestFuncName(t *testing.T) {
	t.Parallel()

	tests := []struct {
		name string
		kind types.Kind
		want string
	}{
		{
			name: "Name",
			kind: types.Builtin,
			want: "WithName",
		},
		{
			name: "Struct",
			kind: types.Struct,
			want: "WithStruct",
		},
		{
			name: "Slice",
			kind: types.Slice,
			want: "AppendSlice",
		},
	}

	for _, test := range tests {
		m := types.Member{
			Name: test.name,
			Type: &types.Type{
				Kind: test.kind,
			},
		}

		assert.Equal(t, test.want, funcName(m))
	}
}
