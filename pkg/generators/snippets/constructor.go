package snippets

import (
	"errors"
	"io"

	"k8s.io/gengo/generator"
	"k8s.io/gengo/types"
)

func GenerateConstructor(w io.Writer, ctx *generator.Context, t *types.Type) error {
	if t == nil {
		return errors.New("nil pointer")
	}

	args := generator.Args{
		"type": t,
	}

	raw := `// New$.type|raw$ is an autogenerated constructor.
func New$.type|raw$(name string) *$.type|raw$ {
	o := &$.type|raw${}
	o.ObjectMeta.Name = name
	return o
}

`
	return writeSnippetWithArgs(w, ctx, raw, args)
}
