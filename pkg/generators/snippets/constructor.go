package snippets

import (
	"github.com/kanopy-platform/code-generator/pkg/generators/tags"
	"k8s.io/gengo/generator"
	"k8s.io/gengo/types"
)

func GenerateEmptyConstructor(t *types.Type) (string, generator.Args) {
	args := defaultGeneratorArgs(t)

	raw := `// New$.type|raw$ is an autogenerated constructor.
func New$.type|raw$() $.pointer$$.type|raw$ {
	o := $.ampersand$$.type|raw${}
	return o
}

`
	return raw, args
}

func GenerateConstructorForObjectMeta(t *types.Type) (string, generator.Args) {
	args := defaultGeneratorArgs(t)

	raw := `// New$.type|raw$ is an autogenerated constructor.
func New$.type|raw$(name string) $.pointer$$.type|raw$ {
	o := $.ampersand$$.type|raw${}
	o.ObjectMeta.Name = name
	return o
}

`
	return raw, args
}

func defaultGeneratorArgs(t *types.Type) generator.Args {
	args := generator.Args{
		"type":      t,
		"pointer":   "",
		"ampersand": "",
	}

	if tags.IsPointerReceiver(t) {
		args["pointer"] = "*"
		args["ampersand"] = "&"
	}

	return args
}
