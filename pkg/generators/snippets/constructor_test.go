package snippets

import (
	"bytes"
	"errors"
	"testing"

	"github.com/stretchr/testify/assert"
	"github.com/stretchr/testify/require"
	"k8s.io/gengo/generator"
	"k8s.io/gengo/types"
)

func TestGenerateConstructor(t *testing.T) {
	t.Parallel()

	ctx, err := newTestGeneratorContext()
	require.NoError(t, err)

	tests := []struct {
		ctx     *generator.Context
		t       *types.Type
		wantErr error
		want    string
	}{
		{
			// nil pointer t
			ctx:     ctx,
			t:       nil,
			wantErr: errors.New("nil pointer"),
		},
		{
			ctx:     ctx,
			t:       newSampleTestType(t, "TestStruct"),
			wantErr: nil,
			want: `// NewTestStruct is an autogenerated constructor.
func NewTestStruct(name string) *TestStruct {
	o := &TestStruct{}
	o.ObjectMeta.Name = name
	return o
}

`,
		},
	}

	for _, test := range tests {
		var b bytes.Buffer
		err := GenerateConstructor(&b, test.ctx, test.t)
		if test.wantErr != nil {
			assert.Error(t, err)
		} else {
			assert.NoError(t, err)
			assert.Equal(t, test.want, b.String())
		}
	}
}
